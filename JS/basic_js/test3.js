// string concatenation

const name = "Alice";
const repoCCount = 10;

console.log("Hello " + name + ", you have " + repoCCount + " repositories.");
// This is old way of concatenation

// Using template literals (ES6 feature)
console.log(`Hello ${name}, you have ${repoCCount} repositories.`);
// This is modern way of concatenation
// It is more readable and easier to use
// especially when dealing with multiple variables and complex strings.
// It is known as string interpolation. i.e. using (`String......${variable which we want to add in the string }..........String`)
// These `` are called backticks.which is also the syntax of modern way of concatenation


console.log();

